#include "fillit.h"

unsigned short	ft_btoi(char *str)
{
	unsigned short	nbr;
	int				i;

	i = 0;
	nbr = 0;
	while (str[i])
	{
		nbr *= 2;
		if (str[i] == '1')
			nbr += 1;
		i++;
	}
	return (nbr);
}
